# Algoritmo que dado un diccionario inicializado, muestra por consola sus valores actuales y luego vacía el diccionario mediante el método clear()

diccionario = {
    "pen": "blue",
    "pencil": "red", 
    "eraser": "white"
}

print(diccionario)  # Muestra por consola los valores actuales del diccionario 

diccionario.clear()

print(diccionario)  # Muestra por consola los valores actuales del diccionario 
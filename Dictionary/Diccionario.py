# A dictionary is a collection which is unordered, changeable and indexed. 
# In Python dictionaries are written with curly brackets, and they have keys and values.
# Un diccionario es una colección que no está ordenada, no es variable y no está indexada.
# En Python los diccionarios están escritos con llaves, y ellos tienen claves y valores.

# Algoritmo que inicializa una variable de tipo Dictionary y la carga con claves y valores para luego mostrarlo por consola.

diccionario = {
    "Año": "2020",
    "Mes": "Julio",
    "Dia": "Miércoles 15"
}

print(diccionario)

# Para conocer la longitud del mismo, es decir cuantos pares de elementos (key-value) posee, uso la funcion len()

print("El diccionario posee", len(diccionario), "pares de elementos")